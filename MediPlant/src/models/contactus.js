const mongoose=require("mongoose");

const ContactSchema=new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    mobile:{
        type:Number,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    subject:{
        type:String,
        required:true
    },
    message:{
        type:String,
        required:true
    }
})

const ContactUs=new mongoose.model("Contact",ContactSchema);
module.exports=ContactUs;